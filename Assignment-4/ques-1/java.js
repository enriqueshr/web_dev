function checkEvenOrOdd(number) {
    if (number % 2 === 0) {
        console.log(`${number} is an even number.`);
    } else {
        console.log(`${number} is an odd number.`);
    }
}

// Example usage:
checkEvenOrOdd(7); // Output: "7 is an odd number."
checkEvenOrOdd(8); // Output: "8 is an even number."
checkEvenOrOdd(9); // Output: "9 is an odd number."
checkEvenOrOdd(13); // Output: "13 is an odd number."